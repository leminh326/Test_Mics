#ifndef IQSXXX_I2C_H_
#define IQSXXX_I2C_H_

// --------------------------------------------------------------------------------------------------------------------
// ----- includes
// --------------------------------------------------------------------------------------------------------------------
#include "stm32wbxx_ll_i2c.h"
#include "stdbool.h"
// --------------------------------------------------------------------------------------------------------------------
// ----- public constant macros
// --------------------------------------------------------------------------------------------------------------------
#define REGISTER_ADDRESS_8 0x01
#define REGISTER_ADDRESS_16 0x02

// --------------------------------------------------------------------------------------------------------------------
// ----- public function macros
// --------------------------------------------------------------------------------------------------------------------


// --------------------------------------------------------------------------------------------------------------------
// ----- public typedefs, structures, unions and enums
// --------------------------------------------------------------------------------------------------------------------
typedef enum
{
  I2C_OK       = 0x00,
  I2C_ERROR    = 0x01,
  I2C_BUSY     = 0x02,
  I2C_TIMEOUT  = 0x03
} IQSxxx_I2C_Status_e;

typedef enum {
	I2C_Repeat_Start = 0,
	I2C_Start = 1,
	I2C_No_Start = 2
}IQSxxx_I2C_Start_e;

typedef enum {
	I2C_No_Stop = 0,
	I2C_Stop = 1
}IQSxxx_I2C_Stop_e;

// --------------------------------------------------------------------------------------------------------------------
// ----- public variables
// --------------------------------------------------------------------------------------------------------------------


// --------------------------------------------------------------------------------------------------------------------
// ----- public function prototypes
// --------------------------------------------------------------------------------------------------------------------

IQSxxx_I2C_Status_e IQSxxx_I2C_Write(I2C_TypeDef *I2Cx,uint32_t slaveAddress, uint16_t regAddress, uint8_t regSIze, uint16_t size,const uint8_t *data, IQSxxx_I2C_Start_e start,IQSxxx_I2C_Stop_e stop);
IQSxxx_I2C_Status_e IQSxxx_I2C_Read(I2C_TypeDef *I2Cx,uint32_t slaveAddress, uint16_t regAddress, uint8_t regSIze, uint16_t size,uint8_t *data, IQSxxx_I2C_Start_e start,IQSxxx_I2C_Stop_e stop);


#endif /* IQSXXX_I2C_H_ */
